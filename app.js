  function processCSVFile(file) {
    if (!file.name.endsWith(".csv")) {
      alert("Пожалуйста, загрузите файл формата .csv");
      return;
    }

    if (fileName) fileName.textContent = file.name;
    if (fileInfo) fileInfo.style.display = "flex";

    const reader = new FileReader();
    reader.onload = function (e) {
      const text = e.target.result;
      parseCSVText(text);
    };
    reader.readAsText(file, "UTF-8");
  }

  // Simple CSV parser supporting quotes and commas
  function parseCSVText(text) {
    const lines = [];
    let row = [""];
    let inQuotes = false;

    for (let i = 0; i < text.length; i++) {
      const char = text[i];
      const nextChar = text[i + 1];

      if (char === '"') {
        if (inQuotes && nextChar === '"') {
          row[row.length - 1] += '"';
          i++; // Skip next quote
        } else {
          inQuotes = !inQuotes;
        }
      } else if (char === ',' && !inQuotes) {
        row.push("");
      } else if ((char === '\r' || char === '\n') && !inQuotes) {
        if (char === '\r' && nextChar === '\n') {
          i++;
        }
        lines.push(row);
        row = [""];
      } else {
        row[row.length - 1] += char;
      }
    }
    if (row.length > 1 || row[0] !== "") {
      lines.push(row);
    }

    if (lines.length < 2) {
      alert("Не удалось прочитать строки в CSV файле.");
      return;
    }

    const headers = lines[0].map(h => h.trim().toLowerCase());
    
    // Find index of matching headers
    const nameIdx = headers.findIndex(h => h.includes("название") || h.includes("name"));
    const addressIdx = headers.findIndex(h => h.includes("адрес") || h.includes("address"));
    const mapsUrlIdx = headers.findIndex(h => h.includes("карты") || h.includes("maps") || h.includes("url") || h.includes("ссылка"));
    const siteIdx = headers.findIndex(h => h.includes("сайт") || h.includes("website") || h.includes("site"));
    const siteTypeIdx = headers.findIndex(h => h.includes("тип") || h.includes("type"));
    const priorityIdx = headers.findIndex(h => h.includes("приоритет") || h.includes("priority"));

    if (nameIdx === -1) {
      alert("Неверный формат CSV. Отсутствует обязательная колонка 'Название'.");
      return;
    }

    leadsData = [];
    for (let i = 1; i < lines.length; i++) {
      const line = lines[i];
      if (line.length <= 1 || !line[nameIdx]) continue;

      const name = line[nameIdx] ? line[nameIdx].trim() : "";
      const address = addressIdx !== -1 && line[addressIdx] ? line[addressIdx].trim() : "Не указан";
      const mapsUrl = mapsUrlIdx !== -1 && line[mapsUrlIdx] ? line[mapsUrlIdx].trim() : "#";
      const website = siteIdx !== -1 && line[siteIdx] ? line[siteIdx].trim() : "Без сайта";
      
      let websiteType = "Без сайта";
      if (siteTypeIdx !== -1 && line[siteTypeIdx]) {
        websiteType = line[siteTypeIdx].trim();
      } else if (website && website !== "Без сайта" && website !== "") {
        if (website.includes("vk.com") || website.includes("instagram") || website.includes("ok.ru") || website.includes("facebook")) {
          websiteType = "Соцсеть";
        } else if (website.includes("taplink") || website.includes("tilda") || website.includes("wix")) {
          websiteType = "Конструктор / Taplink";
        } else {
          websiteType = "Собственный сайт";
        }
      }

      let priority = "high";
      if (priorityIdx !== -1 && line[priorityIdx]) {
        const p = line[priorityIdx].trim().toLowerCase();
        if (p.includes("высок") || p.includes("high")) priority = "high";
        else if (p.includes("средн") || p.includes("med")) priority = "medium";
        else priority = "low";
      } else {
        if (websiteType === "Без сайта" || websiteType === "Соцсеть") {
          priority = "high";
        } else if (websiteType === "Конструктор / Taplink") {
          priority = "medium";
        } else {
          priority = "low";
        }
      }

      // Add to leads array
      leadsData.push({ name, address, mapsUrl, website, websiteType, priority });
    }

    updateLeadsUI();
  }


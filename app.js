      const traffic = parseInt(sliderTraffic.value);
      const conversion = parseFloat(sliderConv.value);
      const price = parseInt(sliderPrice.value);

      valueTraffic.textContent = traffic.toLocaleString("ru-RU");
      valueConv.textContent = conversion.toFixed(1) + "%";
      valuePrice.textContent = "$" + price;


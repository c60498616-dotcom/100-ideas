    if (idea.monetization === "one-time") defaultPrice = 15;
    if (idea.monetization === "subscription") defaultPrice = 10;
    if (idea.category === "services") defaultPrice = 300;

    sliderPrice.value = defaultPrice;
    sliderPrice.min = Math.round(defaultPrice * 0.3);
    sliderPrice.max = Math.round(defaultPrice * 5);
    
    sliderTraffic.value = 5000;
    sliderConv.value = 1.5;

